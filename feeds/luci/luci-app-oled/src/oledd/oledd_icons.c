const struct oledd_icon *oledd_icon_get(const char *name, int size)
{
	const struct oledd_icon *best = NULL;
	size_t i;

	if (!name || !name[0])
		return NULL;

	for (i = 0; g_icons[i].name; i++) {
		if (strcmp(g_icons[i].name, name) != 0)
			continue;
		if (g_icons[i].size == size)
			return &g_icons[i];
		if (!best || g_icons[i].size < size)
			best = &g_icons[i];
	}
	return best;
}

void oledd_icon_draw(int x, int y, const char *name, int size)
{
	const struct oledd_icon *ic = oledd_icon_get(name, size);
	int row, col;

	if (!ic || !ic->rows)
		return;

	for (row = 0; row < ic->h; row++) {
		uint8_t bits = ic->rows[row];

		for (col = 0; col < ic->w; col++) {
			if (bits & (0x80 >> col))
				drawPixel(x + col, y + row, WHITE);
		}
	}
}
